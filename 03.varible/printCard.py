# -*- coding=utf-8 -*-

# 对于input，
# 输入的内容在python3中都是当字符串对待，
# 在python2中会把输入的东西当做代码先执行，可能会引起异常报错
# 所以在python2 中可以使用raw_input代替
# 获取名字
name = input("请输入你的名字：")
# 获取微信
wechatName = input("请输入你的微信号：")
# 打印出来获取的信息
print("==========================================")
print("你的名字是：%s"%name)
print("你的微信号是：%s"%wechatName)
print("==========================================")
