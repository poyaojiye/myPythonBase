test = "0123456789"
print("test是：%s"%test)
print("test[2:5]是：%s"%test[2:5])
print("test[2:]是：%s"%test[2:])
print("test[2:-1]是：%s"%test[2:-1])
print("test[2:-2]是：%s"%test[2:-2])
print("test[2:0]是：%s"%test[2:0])
print("test[2:-1:2]是：%s"%test[2:-1:2])
print("test[2:-1:1]是：%s"%test[2:-1:1])
print("test[-1:0]是：%s"%test[-1:0])
print("test[-1:0:-1]是：%s"%test[-1:0:-1])
print("test[-1::-1]是：%s"%test[-1::-1])
print("test[::-1]是：%s"%test[::-1])
